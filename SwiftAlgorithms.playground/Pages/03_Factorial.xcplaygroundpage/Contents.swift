## Instructions:
Write a function that recieve a number and print the factorial of the number recieved.
Note: Factorial is the product of all positive integers less than or equal to a given positive integer.
 
The factorial of 'n' is also equals to the product of 'n' with the next smaller factorial:
 
 n! = n x (n-1) x (n-2) x (n-3) x ... x 1
 #### n! = n x (n-1)!

 For example:

 0! = 1

 1! = 1

 2! = 2 x (2-1) = 2x1 = 2

 3! = 3 x (3-1) x (3-2) = 3x2x1 = 6

 4! = 4 x (4-1) x (4-2) x (4-3) = 4x3x2x1 = 24

 5! = 5 x (5-1) x (5-2) x (5-3) x (5-4) = 5x4x3x2x1 = 120

